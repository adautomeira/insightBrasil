"""Shared loaders for the local SADIPEM / transferências / population data.

Not a standalone script (leading underscore) — imported by the analysis scripts
in this folder (correlate.py, per_capita.py, debt_to_revenue.py, trends.py,
purposes_and_creditors.py, approval_rates.py, pandemic_spotlight.py) so each one
can focus on its own analysis instead of re-implementing CSV parsing and
Brazilian number formatting.
"""

import csv
import glob
import os
import re

DATA_DIR = os.path.join(os.path.dirname(__file__), "..", "data")


def to_float(value):
    """Parse a Brazilian-formatted number ("1.234.567,89") into a float."""
    value = value.strip()
    if not value:
        return 0.0
    value = value.replace(".", "").replace(",", ".")
    try:
        return float(value)
    except ValueError:
        return 0.0


def sadipem_rows():
    """Yield SADIPEM request rows as dicts with the fields analyses care about.

    `valor` is parsed to float; `year`/`month` come from the "Data" column
    (the date the request was processed, DD/MM/YYYY). Rows whose date can't be
    parsed are skipped — they can't be placed on any time-based comparison.
    """
    for path in glob.glob(os.path.join(DATA_DIR, "sadipemconsultapublicageral*.csv")):
        with open(path, encoding="cp1252") as f:
            for row in csv.DictReader(f, delimiter=";"):
                m = re.match(r"(\d{2})/(\d{2})/(\d{4})", row["Data"].strip())
                if not m:
                    continue
                day, month, year = m.groups()
                yield {
                    "uf": row["UF"].strip(),
                    "interessado": row["Interessado"].strip(),
                    "year": year,
                    "month": month,
                    "status": row["Status"].strip(),
                    "valor": to_float(row["Valor"]),
                    "finalidade": row["Finalidade"].strip(),
                    "credor": row["Credor"].strip(),
                    "tipo_credor": row["Tipo de credor"].strip(),
                }


def transferencias_rows():
    """Yield constitutional-transfer rows as dicts, with the three decêndios summed.

    `total` is the month's full transfer to that município (the three
    "decêndio" instalments combined).
    """
    for path in glob.glob(os.path.join(DATA_DIR, "transferenciamensalmunicipios*.csv*")):
        with open(path, encoding="cp1252") as f:
            for row in csv.DictReader(f, delimiter=";"):
                year = row.get("ANO", "").strip()
                if not year:
                    continue
                yield {
                    "uf": row["UF"].strip(),
                    "municipio": row["Município"].strip(),
                    "year": year,
                    "month": row.get("Mês", "").strip(),
                    "item": row.get("Item transferência", "").strip(),
                    "total": (
                        to_float(row["1º Decêndio"])
                        + to_float(row["2º Decêndio"])
                        + to_float(row["3º Decêndio"])
                    ),
                }


def population_by_uf():
    """Load UF -> population from data/populacao_uf.csv.

    That file is reference data generated by download_population.py — run it
    first if this raises FileNotFoundError.
    """
    path = os.path.join(DATA_DIR, "populacao_uf.csv")
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"{path} not found.\nRun `python3 scripts/download_population.py` first "
            "to generate it from IBGE's population estimates."
        )
    with open(path) as f:
        reader = csv.DictReader(f)
        population_column = reader.fieldnames[1]
        return {row["UF"]: int(row[population_column]) for row in reader}
